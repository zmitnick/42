#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		n = 0;
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		c = n % 10 + '0';
		if (n >= 10)
			ft_putnbr(n / 10);
		write(1, &c, 1);
	}
}
