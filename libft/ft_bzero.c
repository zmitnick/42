#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*str;
	int				i;

	i = 0;
	str = (unsigned char *)s;
	while ((int)n > 0)
	{
		str[i] = 0;
		i++;
		n--;
	}
}
