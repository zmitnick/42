int	ft_atoi(const char *str)
{
	int i;
	int sign;
	int n;

	i = 0;
	n = 0;
	sign = 1;
	while (*(str + i) == '\n' || *(str + i) == '\t' || *(str + i) == '\v' ||
			*(str + i) == '\f' || *(str + i) == '\r' || *(str + i) == ' ')
		i++;
	if (*(str + i) == '+')
		i++;
	if (*(str + i) == '-')
	{
		sign = -1;
		i++;
	}
	while (*(str + i) <= '9' && *(str + i) >= '0')
	{
		n = n * 10 + *(str + i) - '0';
		i++;
	}
	return (sign * n);
}
