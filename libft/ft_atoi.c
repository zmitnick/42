#include "libft.h"

int	ft_atoi(const char *str)
{
	int a;
	int i;
	int aux;
	int neg;

	neg = 0;
	i = 0;
	a = 0;
	if (str[0] == '-')
	{
		neg = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		a = a * 10 + (str[i] - '0');
		i++;
	}
	aux = a;
	if (neg == 1)
		a = 0 - aux;
	return (a);
}
